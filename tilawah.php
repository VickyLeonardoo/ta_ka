<h5 class="rit-titl">Sistematika dan Peraturan Perlombaan</h5>
                        <a class="justify">1.	Peserta bersifat individu atau perorangan.<br>
2.	Pastikan peserta mendaftar dengan menggunakan email aktif. Panitia akan memberikan tautan WhatsApp grup melalui email yang telah didaftarkan.  <br>
3.	Panitia menentukan Maqra’ (ayat yang dibaca) masing-masing peserta.<br>
4.	Peserta wajib membaca Maqra’ yang ditetapkan oleh panitia yang diberikan pada Grup WhatsApp.<br>
5.	Ketentuan bacaan tartil sebagai berikut:<br>
•	Bidang lomba dengan bacaan Mujawwad, yang dinilai adalah Fashahah (kefasihan), hukum tajwid, nagham (irama), adab membaca Al-Qur’an dan suara.<br>
•	Peserta minimal membaca dengan 4 irama, diawali irama bayyati.<br>
•	Durasi lomba maksimal 10 menit.<br>
6.	Peserta wajib menggunakan pakaian yang tertutup dan sopan.<br>
7.	Peserta tidak dapat digantikan selama perlombaan.<br>
8.	Peserta wajib mengunduh nomor peserta pada … dan wajib dipakai saat perlombaan berlangsung.<br>
9.	Peserta wajib memasuki waiting room zoom sesuai dengan link yang disediakan oleh panitia.<br>
10.	 Perserta wajib hadir 10 menit sebelum jadwal perlombaan peserta dimulai.<br>
11.	 Peserta akan dimasukkan ke breakout room zoom sesuai dengan jadwal  masing-masing peserta.<br>
12.	Peserta diberikan toleransi 3x pemanggilan dan bila belum hadir dinyatakan gugur.<br>
13.	Peserta yang sudah selesai tampil, dipersilakan meninggalkan zoom.<br>
14.	Segala kecurangan atau ketidakpatuhan terhadap syarat dan ketentuan lomba akan didiskualifikasi.<br>
15.	Panitia mempunyai hak penuh atas hal-hal yang menyangkut teknis lomba dan hal-hal lain yang tidak terduga.<br>
16.	Keputusan dewan juri bersifat mutlak, mengikat, dan tidak dapat diganggu gugat.<br>

