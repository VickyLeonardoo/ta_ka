                      <br><h5 class="rit-titl"> Deskripsi</h5>
                        <p class="justify">National Tilawah Competition adalah kompetisi bertaraf nasional yang memperhatikan beberapa bidang meliputi ilmu tajwid dan seni bacaan tilawah. Dalam kompetisi ini, peserta akan menghadapi beberapa babak dan akan merebutkan posisi sampai babak final. Kompetisi ini memiliki tujuan yaitu agar peserta dapat mengembangkan pengetahuan dan keterampilan generasi qur’ani serta dapat memahami Al-Qur’an secara keseluruhan dengan baik.</p><br>

