                        <p class="justify">Islamic Speech Contest adalah bidang lomba ceramah bernuansa islami yang diadakan dalam rangka memperingati Maulid Nabi Muhammad SAW. Dalam lomba tersebut, peserta lomba diwajibkan untuk menyampaikan ceramah sesuai dengan tema dan ketentuan yang berlaku. Lomba ini bertujuan untuk menyampaikan dan menyebarluaskan dakwah sebagaimana yang telah diajarkan oleh baginda Rasulullah SAW untuk semua ummatnya.</p><br>

                        <h5 class="rit-titl"> Tema</h5>
                        <p class="justify">Tema dari perlombaan ini adalah “Generasi Islami Yang Berakhlakul Kharimah” dimana melalui tema ini, panitia berharap peserta dapat menyampaikan materi ceramah yang inspiratif dan komunikatif kepada khalayak umum dengan bahasa yang mudah dimengerti. Dengan subtema sebagai berikut:<br>
a)	Suri Tauladan Nabi.<br>
b)	Penerapan Akhlakul Kharimah.<br>
c)	Usaha penerapan Islam Kaffah.<br>
d)	Mencetak Generasi Islami.<br>
e)	Menebar Kebaikan.<br>
</p><br>

